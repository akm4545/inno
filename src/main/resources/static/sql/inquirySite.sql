CREATE TABLE INQUIRYSITE(
	INQUIRYSITE_IDX INT NOT NULL PRIMARY KEY AUTO_INCREMENT,		/* 문의 사이트 인덱스 */
	INQUIRY_IDX INT NOT NULL,										/* 문의 테이블 주키 (외래키) */
	INQUIRYSITE_SITE TEXT,											/* 유지보수 사이트, 참고 사이트 */
	INQUIRYSITE_TYPE CHAR(1) NOT NULL DEFAULT 'R',					/* 사이트 타입 유지보수 사이트 = C, 참고 사이트 = R */
	FOREIGN KEY (INQUIRY_IDX) REFERENCES INQUIRY(INQUIRY_IDX)
	ON DELETE CASCADE
);